fn follows_policy(password: &str, policy: &Policy) -> bool {
    let lower_bound_char = password
        .chars()
        .nth(policy.lower_bound as usize - 1)
        .unwrap();
    let upper_bound_char = password
        .chars()
        .nth(policy.upper_bound as usize - 1)
        .unwrap();

    if lower_bound_char == upper_bound_char {
        return false;
    }

    if lower_bound_char == policy.character {
        return true;
    }

    if upper_bound_char == policy.character {
        return true;
    }

    return false;
}

